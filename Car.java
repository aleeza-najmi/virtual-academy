public class Car {
    public String make;
    public int year;
    public int milesDriven; 
    public boolean needsOilChange; 

    public Car(String make, int year) {
        this.make = make;
        this.year = year;
        this.milesDriven = 0; 
        this.needsOilChange = false;
    }

   

}
